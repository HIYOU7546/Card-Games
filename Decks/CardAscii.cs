    /// <summary>
    /// Returns the ascii art of a card based on the suit and rank provided.
    /// </summary>
    /// <param name="suit"></param>
    /// <param name="rank"></param>
    /// <returns></returns>
    public static string[] GetCardAscii(string suit, string rank)
    {
      if (char.Parse(suit) == 'R') suit = "red";
      else if (char.Parse(suit) == 'B') suit = "black";
      else if (char.Parse(suit) == 'S') suit = "spade";
      else if (char.Parse(suit) == 'C') suit = "club";
      else if (char.Parse(suit) == 'H') suit = "heart";
      else if (char.Parse(suit) == 'D') suit = "diamond";
      else if (char.Parse(suit) == 'F') suit = "faceDown";
      else
      {
        Console.WriteLine("A fatal error has happened while getting the suit of the card.");
        return ["-"];
      }
      if (suit == "faceDown") rank = "Back";
      else if (rank == "JO") rank = "Joker";
      else if (char.Parse(rank) == 'A') rank = "Ace";
      else if (char.Parse(rank) == 'K') rank = "King";
      else if (char.Parse(rank) == 'Q') rank = "Queen";
      else if (char.Parse(rank) == 'J') rank = "Jack";
      else if (char.Parse(rank) == '2') rank = "2";
      else if (char.Parse(rank) == '3') rank = "3";
      else if (char.Parse(rank) == '4') rank = "4";
      else if (char.Parse(rank) == '5') rank = "5";
      else if (char.Parse(rank) == '6') rank = "6";
      else if (char.Parse(rank) == '7') rank = "7";
      else if (char.Parse(rank) == '8') rank = "8";
      else if (char.Parse(rank) == '9') rank = "9";
      else
      {
        Console.WriteLine("A fatal error has happened while getting the rank of the card.");
        return ["-"];
      }
      string cardToCall = suit + rank;
      if (CardArt.TryGetValue(cardToCall, out string[]? card))
      {
        return card;
      }
      else
      {
        Console.WriteLine($"Variable '{cardToCall}' not found.");
        return ["-"];
      }
    }

